    private void TaskButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TaskButtonMouseClicked
        TaskDialogScreen taskDialogScreen = new TaskDialogScreen(this, rootPaneCheckingEnabled);
        
        int projectIndex = ProjectList.getSelectedIndex();
        Project project = (Project) projectsModel.get(projectIndex);
        taskDialogScreen.setProject(project);
        
        taskDialogScreen.setVisible(true);
        
        taskDialogScreen.addWindowListener(new WindowAdapter() {
            public void windowClosed(WindowEvent e) {
                loadTasks(project.getId());
            }
        });
    }//GEN-LAST:event_TaskButtonMouseClicked

    private void TableTasksMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableTasksMouseClicked
        int row = TableTasks.rowAtPoint(evt.getPoint());
        int col = TableTasks.columnAtPoint(evt.getPoint());
        
        Task task = tasksModel.getTasks().get(row);
        
        switch(col) {
            case 3:        
                taskController.update(task);
                break;
            case 4: 
                TaskDialogScreen taskDialogScreen = new TaskDialogScreen(this, rootPaneCheckingEnabled);
                
                break;
            case 5: 
                taskController.removeById(task.getId());
                tasksModel.getTasks().remove(task);
                loadTasks(task.getProjectId());
                break;
        }
    }//GEN-LAST:event_TableTasksMouseClicked

