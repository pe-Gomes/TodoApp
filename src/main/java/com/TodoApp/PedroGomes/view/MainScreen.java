    private void decorationTaskTable() {
        TableTasks.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 14));
        TableTasks.getTableHeader().setBackground(new Color(0,153,102));
        TableTasks.getTableHeader().setForeground(new Color(255,255,255));
        
        // Deadline date
        TableTasks.getColumnModel().getColumn(2).setCellRenderer(new DeadlineCellRenderer());
        
        // Buttons
        TableTasks.getColumnModel().getColumn(4).setCellRenderer(new ButtonColumnCellRenderer("edit"));
        TableTasks.getColumnModel().getColumn(5).setCellRenderer(new ButtonColumnCellRenderer("delete"));
        
        TableTasks.setAutoCreateRowSorter(true);
    }
    
    private void initDataController() {
        projectController = new ProjectController();
        taskController = new TaskController();
    }
    
    private void initComponentsModel() {
        projectsModel = new DefaultListModel();
        loadProjects();
        
        tasksModel = new TaskTableModel();
        TableTasks.setModel(tasksModel);
        
        if(!projectsModel.isEmpty()) {
            ProjectList.setSelectedIndex(0);
            Project project = (Project) projectsModel.get(0);
            
            loadTasks(project.getId());
        }
    }
    
    private void loadProjects() {
        List<Project> projects = projectController.getAll();
          
        projectsModel.clear();
        
        for (int i = 0; i < projects.size(); i++) {
            Project project = projects.get(i);
            projectsModel.addElement(project);
        }
        
        ProjectList.setModel(projectsModel);
    }
    
    public void loadTasks(int projectId) {
        List<Task> tasks = taskController.getAll(projectId);
        tasksModel.setTasks(tasks);
        
        showTableTasks(!tasks.isEmpty());
    }
    
    public void showTableTasks(boolean hasTasks) {
        if (hasTasks) {
            if (EmptyTask.isVisible()) {
                EmptyTask.setVisible(false);
                TaskListPanel.remove(EmptyTask);
            }
            
            TaskListPanel.add(TableTasksScrool);
            TableTasksScrool.setVisible(true);
            TableTasksScrool.setSize(TaskListPanel.getWidth(), TaskListPanel.getHeight());
            
        } else {
            if (TableTasksScrool.isVisible()) {
                TableTasksScrool.setVisible(false);
                TaskListPanel.remove(TableTasksScrool);
            }
            
            TaskListPanel.add(EmptyTask);
            EmptyTask.setVisible(true);
            EmptyTask.setSize(TaskListPanel.getWidth(), TaskListPanel.getHeight());
            
        }
    }
